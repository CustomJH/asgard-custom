_SKILL_BODY_BUDGET = 16_000
_DEFERRED = "asgard-freyja-deferred"
_GENERALISTS = frozenset({"asgard-freyja-brisingamen", "asgard-freyja-hnoss", "asgard-freyja-gersemi"})


def _deferred_pointer(names: list[str]) -> str:
    """합산 예산으로 빠 스킬을 침묵 탈락시키지 않고 분할 재위임 경로를 남긴다."""
    lines: list[str] = []
    for name, body in FREYJA_SKILLS:
        if name not in names:
            continue
        match = re.search(r"^description:\s*(.+)$", body.split("---", 2)[1], re.M)
        description = (match.group(1).strip() if match else "")[:90]
        lines.append(f"- `{name}` — {description}")
    return (
        "# 미주입 스킬 (합산 예산 초과 — 포인터만)\n\n"
        "아래 스킬 본문은 합산 주입 예산 초과로 생략됐다. 해당 도메인이 과업의 중심이면 "
        "상위 Worker에 그 부분을 단독 재위임해 달라고 보고하라. 파일 스킬 로더가 있는 환경에서는 "
        "같은 이름의 스킬을 직접 로드하라.\n\n" + "\n".join(lines)
    )


def resolve_freyja_skills(task: str) -> list[tuple[str, str]]:
    """디스패치 task → 매칭된 전용 스킬 (이름, frontmatter 제거 본문) — 0-LLM 키워드 휴리스틱.

    네이티브 프레이야 자식 세션의 system 에 직접 주입할 본문을 고른다 (파일 스킬 로더 부재 보완).
    무매칭 = 빈 리스트 (fail-open — role 본문 기준으로 진행, role 이 이미 그 폴백을 선언한다).
    복수 매칭은 합산 예산 안에서 도메인 전문 스킬을 우선 주입하고 초과분은 포인터로 축약한다."""
    t = task.lower()

    def matches(keyword: str) -> bool:
        if re.fullmatch(r"[a-z0-9 ._+-]+", keyword):
            return re.search(r"(?<![a-z0-9])" + re.escape(keyword) + r"(?![a-z0-9])", t) is not None
        return keyword in t

    explicit = {name for name, _ in FREYJA_SKILLS if any(matches(k) for k in _SKILL_TRIGGERS[name])}
    if "asgard-freyja-logo-studio" in explicit and any(
        matches(keyword) for keyword in ("interactive showcase", "logo showcase", "로고 쇼케이스")
    ):
        explicit.discard("asgard-freyja-seidr")
    matched = set(explicit)
    if "asgard-freyja-logo-studio" in matched:
        matched.update(
            {
                "asgard-freyja-brisingamen",
                "asgard-freyja-reference-atlas",
                "asgard-freyja-hildisvini",
            }
        )
    stripped = {name: body.split("---", 2)[2].lstrip() for name, body in FREYJA_SKILLS if name in matched}
    order = {name: index for index, (name, _) in enumerate(FREYJA_SKILLS)}
    kept: set[str] = set()
    used = 0
    for name in sorted(
        stripped,
        key=lambda candidate: (candidate not in explicit, candidate in _GENERALISTS, order[candidate]),
    ):
        size = len(stripped[name])
        if used + size <= _SKILL_BODY_BUDGET:
            kept.add(name)
            used += size
    picked = [(name, stripped[name]) for name, _ in FREYJA_SKILLS if name in kept]
    deferred = [name for name, _ in FREYJA_SKILLS if name in stripped and name not in kept]
    if deferred:
        picked.append((_DEFERRED, _deferred_pointer(deferred)))
    return picked


