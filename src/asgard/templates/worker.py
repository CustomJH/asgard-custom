# Worker task → 공통 스킬 매칭 (네이티브 Worker system 주입용 통로 — 모드 A/B 는 파일 스킬이 담당).
# Worker 는 모든 과업이 지나는 표면이라 과주입이 곧 노이즈다 — 트리거는 보수적으로 유지한다.
